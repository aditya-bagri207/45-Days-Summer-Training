#String Method Example

text = "hello world"

# 1. upper() - Converts string to uppercase
print("upper():", text.upper())

# 2. lower() - Converts string to lowercase
print("lower():", text.lower())

# 3. replace() - Replaces one value with another
print("replace():", text.replace("hello", "hi"))

# 4. find() - Finds the position of a character
print("find():", text.find("world"))

# 5. capitalize() - Makes first letter uppercase
print("capitalize():", text.capitalize())