# Integer: Stores whole numbers.
a = 10
print("Integer example")
print("Value:", a)
print("Type:", type(a))
print()

# Float: Stores decimal numbers.
b = 10.5
print("float example")
print("Value:", b)
print("Type:", type(b))
print()

# String: Stores text data.
c = "Aditya"
print("string example")
print("Value:", c)
print("Type:", type(c))
print()

# Boolean: Stores True or False values.
d = True
print("boolean example")
print("Value:", d)
print("Type:", type(d))
print()